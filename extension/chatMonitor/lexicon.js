  // מנוע ההטיות האוטומטי (המכפיל)
  function applyInflections(list) {
    const inflections = [];
    list.forEach(phrase => {
      inflections.push(phrase); 
      // הטיות בסיסיות לזכר/נקבה ורבים
      if (phrase.includes("תה")) inflections.push(phrase.replace("תה", "ת"));
      if (phrase.includes("ך")) inflections.push(phrase.replace("ך", "כם"), phrase.replace("ך", "כן"));
      if (phrase.includes("מבין")) inflections.push(phrase.replace("מבין", "מבינה"), phrase.replace("מבין", "מבינים"));
      if (phrase.includes("מעריך")) inflections.push(phrase.replace("מעריך", "מעריכה"), phrase.replace("מעריך", "מעריכים"));
      if (phrase.includes("שלח")) inflections.push(phrase.replace("שלח", "שלחת"), phrase.replace("שלח", "שלחתם"), phrase.replace("שלח", "שלחתי"));
      if (phrase.includes("בדק")) inflections.push(phrase.replace("בדק", "בדקת"), phrase.replace("בדק", "בדקתם"), phrase.replace("בדק", "בדקנו"));
      if (phrase.includes("חזר")) inflections.push(phrase.replace("חזר", "חזרת"), phrase.replace("חזר", "חזרתם"), phrase.replace("חזר", "חזרת אליי"));
    });
    return [...new Set(inflections)];
  }

